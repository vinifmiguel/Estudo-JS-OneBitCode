const x = parseFloat(prompt("Digite o primeiro número:"))
const y = parseFloat(prompt("Digite o segundo número:"))

const soma = x + y
const subtracao = x - y
const multiplicacao = x * y
const divisao = x / y

alert(
    "Resultados:\n" +
    "\n Soma: " + soma +
    "\n Subtração: " + subtracao +
    "\n Multiplicação: " + multiplicacao +
    "\n Divisão: " + divisao
)
